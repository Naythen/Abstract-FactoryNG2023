package lethalCompany;

public abstract class LethalCompany {
	public Flashlight assembleFlashlight() {
		Flashlight flashlight = createFlashlight();
		flashlight.assemble();
		return flashlight;
	}
	public abstract Flashlight createFlashlight();
	
	public Weapon assembleWeapon() {
		Weapon weapon = createWeapon();
		weapon.assemble();
		return weapon;
	}
	public abstract Weapon createWeapon();
}
